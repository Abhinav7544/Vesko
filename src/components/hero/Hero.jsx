import "./hero.scss";
import { motion } from "framer-motion";

const textVariants = {
  initial: {
    x: -500,
    opacity: 0,
  },
  animate: {
    x: 0,
    opacity: 1,
    transition: {
      duration: 1,
      staggerChildren: 0.1,
    },
  },
  scrollButton: {
    opacity: 0,
    y: 10,
    transition: {
      duration: 2,
      repeat: Infinity,
    },
  },
};
const sliderVariants = {
  initial: {
    x: 0,
  },
  animate: {
    x: "-220%",
    transition: {
      repeat: Infinity,
      repeatType: "mirror",
      duration: 20,
    },
  },
};

const scrollToSection = (sectionId) => {
  const section = document.getElementById(sectionId);
  if (section) {
    section.scrollIntoView({ behavior: "smooth" });
  }
};

const Hero = () => {
  return (
    <div className="hero">
      <div className="wrapper">
        <motion.div
          className="textContainer"
          variants={textVariants}
          initial="initial"
          animate="animate"
        >
          <motion.h1 variants={textVariants}>Vesko</motion.h1>
          <motion.div variants={textVariants} className="buttons">
            <motion.button variants={textVariants}>What we do</motion.button>
            <motion.button variants={textVariants} onClick={()=>  scrollToSection("contact")}>Contact us</motion.button>
          </motion.div>
        </motion.div>

        <div className="imageContainer">
          <img src="/favicon.ico" alt="vesko_logo" />
        </div>
      </div>
    </div>
  );
};

export default Hero;
